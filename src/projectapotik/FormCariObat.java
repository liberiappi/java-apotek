    private void ConnectDB(){
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost/dbapotik";
            String user = "root";
            String pass = "";
            
            conn = DriverManager.getConnection(url, user, pass);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi gagal" + e.getMessage());
            System.exit(0);
        }
    }
    
    private void displayData(){
        
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Kode Obat");
        model.addColumn("Nama Obat");
        model.addColumn("Harga Beli");
        model.addColumn("Harga Jual");
        model.addColumn("Stok");
        
        try {
            String sql = "SELECT * FROM tbobat";
            stm = conn.createStatement();
            rs = stm.executeQuery(sql);
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString("kodeobat"),
                    rs.getString("namaobat"),
                    rs.getString("hargabeli"),
                    rs.getString("harga1"),
                    rs.getString("stok")
                });
            }
            
            jTableObat.setModel(model);
            
            jTableObat.setRowHeight(30);
            
            jTableObat.setFont(new Font("", 0, 20));
            jTableObat.getTableHeader().setFont(new Font("", 1, 18));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
    private void cariData(){
        
        DefaultTableModel model = new DefaultTableModel();
        
        String kategori = jCmbKategori.getSelectedItem().toString();
        String keyword = jTxtCari.getText();
        
        switch(kategori){
            case "Kode Obat":
                kategori = "kodeobat";
                break;
            case "Nama Obat":
                kategori = "namaobat";
                break;
            case "Kategori Obat":
                kategori = "namakategori";
                break;
            default:
                break;
        }
        
        try {
            String sql = "SELECT * FROM tbobat WHERE LOWER(" + kategori + ") LIKE LOWER('%" + keyword + "%')";
            stm = conn.createStatement();
            rs = stm.executeQuery(sql);
            
            // System.out.println(sql);
            
            model.addColumn("Kode Obat");
            model.addColumn("Nama Obat");
            model.addColumn("Harga Beli");
            model.addColumn("Harga Jual");
            model.addColumn("Stok");
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString("kodeobat"),
                    rs.getString("namaobat"),
                    rs.getString("hargabeli"),
                    rs.getString("harga1"),
                    rs.getString("stok")
                });
            }
            
            jTableObat.setModel(model);
            
            stm.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih kategori pencarian terlebih dahulu!");
        }
        
    }
    
