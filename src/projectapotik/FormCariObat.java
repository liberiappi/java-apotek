    private void jOkHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jOkHapusActionPerformed
        // TODO add your handling code here:
        int pesan = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (pesan == JOptionPane.YES_OPTION) {
            
            int row, col;
            String kodeobat;
            
            row = jTableObat.getSelectedRow();
            col = 0;
                    
            kodeobat = jTableObat.getModel().getValueAt(row, col).toString();
            
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            
            try {
                String sql = "DELETE FROM tbobat WHERE kodeobat = '" + kodeobat + "'";
                stm = conn.createStatement();
                stm.executeUpdate(sql);
                displayData();
                stm.close();    
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
                
            }
            
        }
        
    }//GEN-LAST:event_jOkHapusActionPerformed

